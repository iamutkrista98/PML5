        <h1>RETURNS POLICY</h1>
        <div class="conditions">
            <p class='top'>
                At Utkrista's Daily Bread Quintessential(DBQ MART), our approach to credits and returns has remained simple and is aimed at unquestionable customer satisfaction. We ask, as part of our cooperative commitment and are dedicated to improving consumer food standards. Our customers work with us with compliance with the revisions that have been made to this Credit and Returns Policy. DBQ MART will continue to return the credit if any product is not picked up on by the customer behalf. After pickup however, our credit and return policy is limited within certain guidelines.
            </p>
            <br>

            <h2 class="section-title">I. OUR PRODUCT RETURN & CREDIT POLICY MODUS OPERANDI: </h2>
            <p class="mode">
                • Ensures product consistency and quality. <br> • Reduction of product loss. <br> • Speeds the processing and ensures accuracy on all requests for credit. <br> • Ensures that product meets the Food Safety Regulations. <br> • Control product returns so that any unfit or substandard product is channelled correctly

            </p>

            <h2 class="section-title">II. AT THE TIME OF PICKUP, YOU SHOULD KNOW:</h2>
            <p>
                • The handler at the pickup point will gladly help you verify that the items delivered agree with your invoice. <br> • Should you choose to return any product, you can check on it at the time of pickup and issue a Credit Request which will process the request within 2 business days. <br> • If a product is short at pickup, the handler present will make an adjustment on the invoice. <br> • If a product is damaged, the handler will make an adjustment on your invoice. <br> • Once you are satisfied with your order, please pay/sign the invoice as received.
            </p>
            <br>
            <p>
                OUR RETURN POLICY AFTER TIME OF PICKUP IS LIMITED. AN ITEM MAY BE RETURNED, AFTER PICKUP, ONLY IF IT MEETS THE FOLLOWING IMPORTANT FOOD SAFETY GUIDELINES

            </p>
            <br>
            <h2 class="section-title">III. TIME: </h3>
                <p>
                    • Make all returns & credit requests within these time frames. <br> • Produce & Perishables: Point of Delivery <br> • Non-Stock Order Items: Non-returnable<br> • Frozen items: 2 days <br> • Dry Goods: 2 days
                </p><br>
                <h2 class="section-title">IV. PRODUCE & PERISHABLES: </h3>
                    <p>
                        • Please inspect all ready-to-eat, produce, dairy, fresh meat, seafood and poultry products thoroughly at time of delivery as these product types are only returnable at time of delivery. This is due to potential product integrity issues and food safety concerns resulting from excessive handling and potential temperature control fluctuations while products have been out of our control.
                    </p><br>


                    <h2 class="section-title">V. CHEMICALS:</h2>
                    <p>

                        • Treat chemical returns with extra attention. A pickup handler must inspect all chemical products for damage and/or leakage, before a pickup and return can be processed. NON-STOCK ORDER ITEMS <br> • Non-stock order items have been purchased especially for you. As they can’t be resold, they are not returnable except when damaged or of questionable quality at time of pickup. <br> • To return non-stock items after pickup, following an inspection, an authorisation is needed by a designated DBQ MART Representative.
                    </p><br>

                    <h2 class="section-title">VI. QUALITY:</h2>
                    <p>

                        • At DBQ MART, we are dedicated to providing the finest and quality products available, to your peace of mind and deliver service you expect and depend upon.
                    </p><br>
                    <h2 class="section-title">VII. PACKAGING:</h2>
                    <p>

                        • Products are returnable for full credit only when they are in the original package free of markings or damage.
                    </p><br>
                    <h2 class="section-title">VIII. TEMPERATURE:</h2>
                    <p>

                        • All chilled items must be returned at point of delivery. <br> • Should a product present a defect or not meet expected Quality standards, Customer Service in DBQ MART should be contacted for guidance.
